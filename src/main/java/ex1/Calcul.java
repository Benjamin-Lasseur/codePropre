package ex1;

public class Calcul {

	/**Cette méthode effectue une somme
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return
	 */
	public int sommer(int a, int b){
		return a+b;
	}
	
	/**Cette méthode effectue une soustraction
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
